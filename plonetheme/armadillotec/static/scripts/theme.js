/* //////////// Menu Hover effect //////////// */ 

function hover(elem){
    elem.style.backgroundColor = '#FF4800';
    elem.style.width = '115px';
    elem.style.height = '51px';
}

function leave(elem){
    elem.style.backgroundColor = '#727e40';
    elem.style.width = '95px';
    elem.style.height = '40px';
}

function leave1(elem){
    elem.style.backgroundColor = '';
    elem.style.width = '95px';
    elem.style.height = '40px';
}
